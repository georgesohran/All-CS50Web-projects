file = input()
i = "image/"

if file.endswith("gif"):
    print(i+"gif")
if file.endswith("jpg"):
    print(i+"jpg")
if file.endswith("jpeg"):
    print(i+"jpeg")
if file.endswith("png"):
    print(i+"png")
if file.endswith("pdf"):
    print(i+"pdf")
if file.endswith("txt"):
    print(i+"txt")
if file.endswith("zip"):
    print(i+"zip")

